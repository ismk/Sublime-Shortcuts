CURSOR MOVEMENT
_______________

ACTION                                                           KEYSTROKE
======                                                          ===========
Move Left / Right                                               Arrow Keys(u/d/l/r)

Move Extreme Up/Down/Left/ Right                                ⌘ (CMD) + Arrow Keys(u/d/l/r)

Move One Word At a Time                                         ⌥ (ALT) + Arrow Keys(l/r)
